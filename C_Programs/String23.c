///////////////////////////////////////////HEADER_FILES//////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////HELPER_FUNCTION//////////////////////////////////////////////////

char SpaceCount(char *str)
{
	int iCnt =0;
	while(*str != '\0')
	{
		if(*str == ' ')
		{
			iCnt++;
		}
		str++;
	}
	return iCnt;
}
///////////////////////////////////////////ENTRY_POINT_FUNCTION////////////////////////////////////////////

int main()
{
	
	char Arr[20];
	int iRet =0;
	
	printf("Enter string\n");
	scanf("%[^'\n']s",Arr);
	
	iRet =SpaceCount(Arr);
	printf("Spaces are : %d\n",iRet);
	
	return 0;
}