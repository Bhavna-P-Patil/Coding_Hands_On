//  Program to convert capital to small letter;
///////////////////////////////////////////HEADER_FILES//////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////HELPER_FUNCTION//////////////////////////////////////////////////

char CapitalToSmall(char c)
{
	if((c >= 'A')&&(c <= 'Z'))
	{
		return c + 32;
	}
	
}
///////////////////////////////////////////ENTRY_POINT_FUNCTION////////////////////////////////////////////

int main()
{
	char ch = '\0',cRet = '\0';
	
	printf("Enter character\n");
	scanf("%c",&ch);
	
	cRet = CapitalToSmall(ch);
	
	printf("Small letter is : %c\n",cRet);
	return 0;
}