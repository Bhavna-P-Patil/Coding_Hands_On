//  Accept string from user and count Capital letter in the string ;
///////////////////////////////////////////HEADER_FILES//////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////HELPER_FUNCTION//////////////////////////////////////////////////

char CountCapital(char *str)
{
	int iCnt =0;
	while(*str != '\0')
	{
		if((*str >= 'A')&&(*str <= 'Z'))
		{
			iCnt++;
		}
		str++;
	}
	return iCnt;
}
///////////////////////////////////////////ENTRY_POINT_FUNCTION////////////////////////////////////////////

int main()
{
	int iRet = 0;
	char Arr[20];
	
	printf("Enter string\n");
	scanf("%[^'\n']s",Arr);
	
	iRet = CountCapital(Arr);
	
	printf("Capital letter are : %d\n",iRet);
	return 0;
}