//  Check whether letter is capital or not ;
///////////////////////////////////////////HEADER_FILES//////////////////////////////////////////////////////

#include<stdio.h>
#include<stdbool.h>

///////////////////////////////////////////HELPER_FUNCTION//////////////////////////////////////////////////

bool CheckCapital(char c)
{
	if((c >= 'A')&&(c <= 'Z'))
	{
		return true;
	}
	else
	{
		return false;
	}
}
///////////////////////////////////////////ENTRY_POINT_FUNCTION////////////////////////////////////////////

int main()
{
	char ch = '\0';
	bool bRet = false;
	
	printf("Enter character\n");
	scanf("%c",&ch);
	
	bRet = CheckCapital(ch);
	
	if(bRet == true)
	{
		printf("It is small\n");
	}
	else
	{
		printf("It is not small\n");
	}
	return 0;
}